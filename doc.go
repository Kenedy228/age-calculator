// Package age_calculator предоставляет функции для вычисления возраста
// на основе календарных дат.
//
// Библиотека работает только с датами (год, месяц, день) и игнорирует
// время суток и часовые пояса. Все входные значения нормализуются,
// поэтому результат не зависит от временной зоны.
//
// Основная функция:
//
//   - AgeAt — вычисляет возраст в полных годах на заданную дату.
//
// Особенности:
//
//   - Возраст определяется по календарной дате, а не по точному моменту времени.
//   - Для даты рождения 29 февраля:
//   - в високосный год днем рождения считается 29 февраля;
//   - в невисокосный год — 1 марта.
//   - Если дата рождения позже даты расчета, возвращается ошибка.
